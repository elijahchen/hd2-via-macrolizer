// Create reverse mapping (byte to keycode)
const byteToKey = {};
for (const [key, value] of Object.entries(basicKeyToByte)) {
  // Prefer shorter key names
  if (!byteToKey[value] || key.length < byteToKey[value].length) {
    byteToKey[value] = key;
  }
}
